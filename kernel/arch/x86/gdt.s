.global load_gdt
.type load_gdt, @function
load_gdt:
    pushl %ebp
    movl %esp, %ebp
    movl 8(%ebp), %eax

    lgdt (%eax)
    ljmp $0x08, $1f
1:  movw $0x10, %ax
    movw %ax, %ds
    movw %ax, %es
    movw %ax, %fs
    movw %ax, %gs
    movw %ax, %ss

    leave
    ret
.size load_gdt, . - load_gdt
